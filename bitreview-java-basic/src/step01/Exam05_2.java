//정수의 리터럴 - 자릿수 표기
package step01;

public class Exam05_2 {
    public static void main(String[] args) {
        //_를 사용하여 읽기 쉽도록 자릿수 표시 가능. 위치는 마음대로
        System.out.println(123456789);
        System.out.println(123_456_789);
        System.out.println(12_45_67_89);

        System.out.println(0b111_0000);
        
        
        
        
    }
}